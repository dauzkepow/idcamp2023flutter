/*
  - OOP = paradigma yang berdasarkan pada konsep objek 
    yang memiliki atribut serta dapat melakukan operasi atau prosedur tertentu.
  - 4 (empat) pilar dalam object oriented programming :
    1. Encapsulation = kondisi dimana status atau kondisi di dalam class, dibungkus, dan bersifat privat
       artinya objek lain tidak bisa mengakses property secara langsung
    2. Abstraction = objek hanya menunjukkan operasinya secara high-level
       cukup tahu method apa saja yang dibuat oleh class tersebut (method kosongan)
    3. Inheritance = mewarisi property kelas induk (parent class) ke kelas anak (child class)
    4. Polymorphism = objek class dapat memiliki bentuk atau implementasi 
       yang berbeda-beda pada satu methode yang sama
  - Class merupakan blueprint untuk mendefinisikan karakteristik dari sebuah objek
  - Di dalam class, variabel = property, fungsi = method
  - Abstract class = gambaran umum dari sebuah kelas, tidak dapat direalisasikan ke sebuah objek
  - Mixin = untukmenggabungkan dan memanfaatkan property/,ethod dari beberapa kelas
  - Extentsion method digunakan untuk membyat method tambahan pada kelas yang sudah ada
*/
