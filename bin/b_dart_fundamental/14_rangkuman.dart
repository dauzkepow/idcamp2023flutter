/*
  - comments untuk menuliskan komentar
  - jenis komentar :
    // = Single-line comment atau komentar satu baris
    /* */ = Multi-line comment di mana kita bisa menuliskan beberapa baris
    /// = Documentation comment
  - Instruksi atau perintah kode yang diawali dengan comment akan dilewati ketika proses kompilasi
  - Variabel digunakan untuk menyimpan nilai
    bisa dedefinisikan secara implisit dengan keyword var
    cara penulisan :
    tipevariabel namaVariabel = nilaiVariabel;
    
  - Tipe data :
    int = bilangan bulat
    double = bilangan desimal
    num = menampung bilangan bulat (int) dan bilangan desimal (double)
    bool = true dan false
    String = teks
    Collection = menyimpan beberapa objek dalam satu variabel
    dynamic = menyimpan tipe data jenis apapun
  - Operator Logika :
    || OR = mengembalikan nilai true jika salah satu ekspresi bernilai true
    && AND = mengembalikan nilai true jika semua ekspresi bernilai true.
    ! NOT = melakukan negasi pada ekspresi yang diberikan
  - Function = blok kode yang dapat menghasilkan output dari input tertentu.
  - const dan final = menyimpan nilai yang tidak akan berubah selama program berjalan.
    const = compile time constants nilai tersebut harus sudah diketahui sebelum program dijalankan.
    final = nilainya masih bisa diinisialisasi ketika aplikasi berjalan.
  - Null Safety = membatasi penggunaan nilai null yang rawan menyebabkan crash

  video pembahasan dari dicoding :
  https://www.youtube.com/watch?v=P8JJDD-e0Nc&t=2s
