/*
  - Informasi terkait aplikasi Android yang ingin di-build tersedia pada berkas :
    android/app/src/main/AndroidManifest.xml
    pada berkas tersebut bisa mengatur nama, ikon, perizinan aplikasi, dll

  - format build Android :
    .apk = flutter build apk        // Untuk format apk
    .aab = flutter build appbundle  // Untuk format aab

  - Konfigurasi untuk aplikasi iOS tersedia pada berkas :
    ios/Runner/Info.plist
    bisa mengatur nama aplikasi, ikon, dan konfigurasi lainnya
    .ipa = flutter build ios

  - Konfigurasi untuk aplikasi web tersedia pada berkas :
    web/manifest.json
    bisa mengatur nama aplikasi, ikon, dan konfigurasi lainnya
    web = flutter build web

