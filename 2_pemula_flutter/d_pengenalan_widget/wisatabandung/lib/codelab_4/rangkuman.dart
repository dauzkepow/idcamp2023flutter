/*
  Rangkuman Materi
  - Pada Flutter semuanya adalah widget
  - Widget merupakan komponen yang perlu saling disusun untuk membangun aplikasi Flutter
  - Widget pada Flutter terbagi menjadi StatelessWidget dan StatefulWidget.
  - State sendiri merupakan data yang ada pada suatu widget 
    dan dapat berubah sesuai interaksi pengguna.
  - Scaffold merupakan sebuah widget yang digunakan untuk membuat tampilan dasar 
    atau kerangka material design pada aplikasi Flutter. 
    Di dalam Scaffold, kita bisa menambahkan widget AppBar, Body, dan FloatingActionButton.
  - Container merupakan widget yang digunakan untuk menampung widget lain.
    Di dalam Container kita bisa menambahkan style, shape (bentuk), 
    dan dekorasi lain pada child widget-nya.
  - Widget Padding untuk menambahkan jarak antara konten dengan border-nya.
  - Center digunakan untuk membuat child widget berada di posisi tengah.
  - Widget Row = menyusun beberapa widget secara horizontal
  - Widget Column = menyusun beberapa widget secara vertikal
  - Flutter menyediakan beberapa tampilan tombol (button), seperti :
    ElevatedButton, TextButton, OutlinedButton, IconButton, dan DropdownButton.
  - Widget untuk menerima input dari pengguna, di antaranya :
    TextField, Switch, Radio, dan Checkbox.
  - Widget Image digunakan untuk menampilkan gambar dari internet atau aset proyek.
  - Kita dapat menambahkan font sendiri sebagai aset lalu menampilkannya menggunakan 
    widget TextStyle.
  - ListView digunakan untuk menampilkan beberapa item dalam bentuk baris atau kolom 
    yang bisa di-scroll.
  - Flutter menyediakan widget Flexible dan Expanded yang dapat memberikan ukuran 
    secara fleksibel dan menempati ruang yang tersedia.
  - Kita dapat memanfaatkan MediaQuery atau widget LayoutBuilder untuk 
    mendapatkan ukuran layar aplikasi yang dijalankan.
  - Dengan ini, kita bisa menentukan breakpoint untuk membuat tampilan yang responsif.
  - Flutter dapat melakukan navigasi antar halaman dengan memanfaatkan Navigator.
    Navigator.push() = pindah ke halaman lain
    Navigator.pop() = kembali ke halaman sebelumnya
  
  - Video Pengenalan Widget = https://youtu.be/O8D-mriLYuw?si=oSq5OBlKDnpIjijM
*/
