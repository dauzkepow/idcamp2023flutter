
/*
Struktur Project Flutter
  - android/
    mengatur konfigurasi untuk aplikasi android. Di dalamnya terdapat file gradle, 
    AndroidManifest, dll. 
  - ios/
    tempat configurasi untuk iOS agar aplikasi jalan di iphone
  - build/
    ketika build project flutter hasil build ada pada folder ini
    ketika membuat file APK hasil file tersebut ada di dalam folder ini
    dan akan terhapus jika menjalankan flutter clean
  - lib/
    merupakan folder utama ketika mengerjakan project flutter
    seluruh code flutter berada di folder ini
  - test/
    tempat menyimpan source code testing

*/