/*
  - Struktur project Flutter memiliki beberapa folder utama yang penting untuk kita ketahui, 
    yaitu:
    a. android/ = konfigurasi aplikasi untuk platform Android.
    b. ios/ = konfigurasi aplikasi untuk platform iOS.
    c. build/ = hasil build dari aplikasi Flutter, misalnya seperti file APK, IPA, \
                atau folder web yang bisa di-host.
    d. lib/ = Folder utama untuk mengerjakan project Flutter. 
              Seluruh source code Flutter berada pada folder ini.
    e. test/ = source code untuk melakukan testing aplikasi Flutter secara otomatis.

  - Ketika pertama kali membuat project Flutter, kita akan diberikan 
    aplikasi starter bertema counter. Kode starter ini memiliki contoh dan komentar 
    yang menjelaskan bagaimana sebuah aplikasi Flutter disusun.

  - Flutter memiliki package manager bernama pub.
    Dengan package manager, kita dapat menambahkan package/library 
    yang dibuat oleh developer lain ke dalam project kita.

