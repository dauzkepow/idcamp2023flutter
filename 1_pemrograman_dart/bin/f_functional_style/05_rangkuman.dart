/*
  - Functional Programming (FP) merupakan paradigma pemrograman 
    di mana proses komputasi didasarkan pada fungsi matematika murni
  - Functional Programming ditulis dengan gaya deklaratif yang berfokus pada "what to solve"
    dibandingkan "how to solve" pada gaya imperatif
  - Terdapat beberapa konsep dan karakteristik functional programming :
    1. Pure Functions = fungsi bergantung pada argumen dan parameter yang dimasuukan ke dalamnya
    2. Recursion = fungsi yang tidak memiliki konsep perulangan sehingga hanya fokus perintah dasar
    3. Immutable Variable = fungsi bersifat immutable, tidak bisa mengubah sebuah variabel
       ketika sudah diinisalisasi
    3. First-Class Citizen = fungsi memiliki sifat yang setara dengan komponen pemrograman lain
    4. Higher-Order Function = fungsi yang menggunakan fungsi lain sebagai argumen
       dan juga dapat mengembalikan fungsi
  - Anonymous Function (Lambda) = fungsi yang tidak memiliki nama
  - CLosure = fungsi yang dapat mengakses variabel di dalam lexical scope-nya
 */

