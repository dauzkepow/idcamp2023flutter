//Paradigma Object Oriented Programming
/*
  - dart merupakah bahasa pemrograman Object
  - OOP = paradigma pemrograman yang banyak digunakan untuk pengembangan aplikasi
  - berdasarkan pada konsep objek yang memuiliki atribut serta dapat melakukan prosedur tertentu
  - bisa mudah memvisualisasikan objek yang ada di dunia nyata ke program komputer

  Contoh :
  - bayangan kucing = objek
  - objek kucing memiliki karakteristik (attribute atau properties):
    - warna bulu
    - usia
    - berat badan
  - bisa melakukan beberapa hal (fungsi atau method) :
    - makan
    - tidur
    - bermain
  
  --- 4 Pilar Pemrograman Berorientasi Objek : ---
    1. Ecapsulation (Isolasi)
       kantong yang menyimpan sesuatu
       untuk mengumpulkan data dan metode yang berhubungan ke dalam sebuah objek
       saat menggunakan ecapsulation, data diisolasi atau tidak dapat diakses langsung dari luar
       objek lain tidak bisa mengakses atau mengubah nilai property secara langsung
       Pada contoh kucing tidak bisa langsung mengubah berat badan kucing,
        namun bisa menambahkannya melalui fungsi atau method makan

    2. Abstaction (Abstrak)
       Penerapan alami dari eksapsulasi
       sebuah objek hanya menunjukkan operasinya secara high-level
       misalnya cukup tahu bagaimana seekor kucing makan, 
        namun tidak perlu tahu seperti apa metabolisme biologis dalam tubuh kucing yang membuat berat badan bertambah

    3. Inheritance (Pewarisan)
       beberapa objek bisa memiliki beberapa karakteristik atau perilaku yang sama, namun mereka bukan objek yang sama
       disinilah inheritance atau pewarisan berperan
       Kucing memiliki sifat dan perilaku yang umum dengan hewan lain, warna, berat, dll
       kucing sebagai objek turunan (subclass) mewarisi semua sifat dan perilaku dari objek induknya (superclass)
       begitu juga dengan ikan juga mewarisi sifat dan perilaku yang sama, namun ikan bisa berenang sementara kucing tidak

    4. Polymorphism (Banyak Bentuk)
       Objek dapat memiliki bentuk atau implementasi berbeda-beda pada metode yang sama
       semua hewan bernafas, namun tentu kucing dan ikan memiliki cara bernafas yang berbeda
       perbedaan bentuk atau cara pernafasan tersebut merupakan contoh dari polymorphism






*/
