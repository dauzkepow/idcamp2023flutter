/*
  - Type system merupakan seperangkat aturan yang menetapkan properti atau tipe ke berbagai
    konstruksi program komputer, seperti variabel, expression, fungsi, atau modul
  - Type system ini memberikan standar kategori tersirat yang digunakan programmer untuk
    tipe data, struktur data, dan komponen lainnya
  - Pada Dart memiliki type system-nya dengan sound type system dengan maksud program akan
    selalu mengevaluasi nilai yang tidak cocok dengan jenis tipenya
  - Manfaat adanya sound type system Dart :
    1. Menemukan bug terkait tipe data pada saat compile time
    2. Kode lebih mudah dibaca dan dikelola
    3. Kompilasi ahead of time (AOT) yang lebih baik
  - Pengertian Generic secara umum adalah konsep yang digunakan untuk menentukan tipe data
    yang akan digunakan
  - Manfaat Generic pada Dart :
    1. Type Safety = objek akan membatasi tipe data yang bisa digunakan ke dalam objek tersebut
    2. Mengurangi duplikasi kode sehingga dapat mempermudah dalam membuat interface baru
  - Dart mendukung type inference, dimana memiliki analyzer yang dapat menyimpulkan tipe 
    untuk field, method, variabel lokal, dan beberapa tipe argumen generic lainnya

*/
