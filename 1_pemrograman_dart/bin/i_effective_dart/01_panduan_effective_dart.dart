/*
  Guidelines dari Effective Dart dibagi menjadi empat bagian sesuai dengan fungsinya.
  1. Style Guide
     mendefinisikan aturan untuk meletakkan dan mengatur kode. 
     Panduan ini juga menentukan bagaimana format penamaan sebuah dentifier, 
     apakah menggunakan camelCase, _underscore, dll
  2. Documentation Guide
     Panduan ini terkait tentang apa yang boleh dan tidak ada di dalam komentar.
     baik itu komentar dokumentasi atau komentar biasa
  3. Usage Guide
     Panduan ini mengajarkan bagaimana memanfaatkan fitur bahasa secara terbaik
     untuk menerapkan perilaku. Penggunaan statement atau expression akan dibahas
     disini
  4. Design Guide
     Panduan dengan cakupan terluas namun paling tidak mengikat. Panduan ini mencakup
     bagaimana mendesain API library yang konsisten dan bisa digunakan

  Effective Dart memiliki banyak rules. Masing-masing aturan akan diawali dengan kata kunci
  untuk menentukan bagaimana sifat dari aturan tersebut. Lima kata kunci tersebut antara lain :
  1. DO
     Ketika aturan diawali DO maka praktik tersebut harus selalu diikuti
  2. DON'T
     Bukan merupakan hal yang baik untuk diterapkan
  3. PREFER
     praktik yang harus diikuti. Namun mungkin ada keadaan dimana lebih masuk akal untuk
     melakukan sebaliknya. Pastinya Anda memahami konsekuensi ketika Anda mengabaikan
     aturan ini
  4. AVOID
     Panduan ini menjelaskan hal-hal yang tidak boleh dilakukan, namun memungkinkan 
     ada alasan bagus untuk melakukannya pada beberapa kejadian
  5. CONSIDER
     Praktik yang bisa Anda ikuti atau tidak diikuti, tergantung pada keadaan dan preferensi
     Anda sendiri
  
  - Meskipun ada banyak sekali aturan dan pedoman yang perlu diikuti dalam Effective Dart,
    kita tidak perlu khawatir dengan peraturan yang sangat ketat. Karena sebagian besar
    panduan yang ada bisa dibilang merupakan common sense dalam membuat program, bahkan jika
    tidak tertulis sekalipun. Selain itu konvensi bertujuan supaya kode kita menjadi lebih
    bagus, mudah dibaca, tentunya dipelihara
*/
