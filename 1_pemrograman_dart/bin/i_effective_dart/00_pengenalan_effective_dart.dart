  - Pada dasarnya sebagai programmer, khususnya yang bekerja dalam sebuah tim mayoritas waktu
    kita akan digunakan untuk membaca kode daripada menulisnya.
  - Untuk itulah hadir coding convention yaitu panduan untuk bahasa pemrograman tertentu 
    yang merekomendasikan gaya pemrograman, praktik, dan metode untuk setiap aspek program
    yang ditulis dalam bahasa tersebut
  - Konvensi ini biasanya meliputi indentasi, komentar, deklarasi, penamaan, arsitektur, dll

  - code convention sangat penting bagi programmer karena beberapa alasan berikut :
    1. 40% - 80% biaya dari sebuah perangkat lunak digunakan untuk pemeliharaan (Maintenance)
    2. Sangat jarang suatu perangkat lunak dipelihara seterusnya oleh penulis aslinya.
    3. Code convention meningkatkan keterbacaan kode, memungkinkan programmer untuk memahami
       kode baru dengan lebih cepat dan menyeluruh
    4. Source code lebih tertata rapi dan bersih sebagai sebuah produk.

  - Dart juga telah mengembangkan coding convention yang bertujuan supaya kita bisa menulis 
    kode yang konsisten, kuat, dan cepat. Convention Dart ini dikenal dengan Effective Dart.
    Effective Dart dibuat untuk mewujudkan dua hal :
    1. Be Consistent (Konsisten)
       Ketika membahas tentang formatting akan banyak argumen subjektif tentang mana yang
       lebih baik. Namun konsistensi tentunya akan sangat membantu secara objektif
    2. Be brief (Ringkas)
       Dart dirancang supaya familiar dengan bahasa pemrograman lain seperti C, Java, 
       JavaScript, dll. Namun Dart ditambah dengan fitur-fitur lain yang belum ditawarkan
       bahasa lain. Jika ada banyak cara untuk mengungkapkan sesuatu, tentunya Anda akan
       menggunakan cara yang paling ringkas bukan???
    
