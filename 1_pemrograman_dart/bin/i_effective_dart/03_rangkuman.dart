/*
  - Kita sudah mempelajari code convention Dart
    tujuannya agar tidak hanya menuliskan kode saja, tapi juga menuliskan kode
    yang berkualitas sesuai dengan standar yang ada.

  - Coding Convention = panduan yang merekomendasikan gaya pemrograman, praktik, dan metode
    untuk setiap aspek program yang ditulis dalam bahasa tertentu
  - Alasan code convention :
    1. Sebagian besar waktu dalam mengembangkan aplikasi dihabiskan untuk maintenance
    2. Sangat jarang suatu software dipelihara seterunya oleh penulis aslinya
    3. Meningkatkan keterbacaan kode dan memahami kode baru dengan lebih cepat
    4. Supaya souce code lebih tertata rapi dan bersih
  
  - Panduan Effective Dart dibagi menjadi empat bagian sesuai dengan fungsinya, antara lain :
    1. Style Guide
       mendefinisikan aturan untuk meletakkan dan mengatur kode
    2. Documentation Guide
       Bagaimana memberikan komentar pada kode
    3. Usage Guide
       Memanfaatkan fitur bahasa secara terbaik untuk menerapkan perilaku
    4. Design Guide
       Mendesain API library yang konsisten dan bisa digunakan
  
  - Setiap panduan akan diawali dengan lima kata kunci ini, antara lain :
    1. DO = aturan yang harus selalu diikuti
    2. DON'T = aturan yang tidak baik untuk diikuti
    3. PREFER = aturan yang harus diikuti, namun ada keadaan dimana lebih masuk akan untuk
       melakukan sebaliknya
    4. AVOID = aturan yang sebaiknya dihindari atau tidak boleh dilakukan
    5. CONSIDER = aturan yang bisa diikuti atau tidak diikuti, tergantung pada keadaan dan
       preferensi sendiri.
*/
